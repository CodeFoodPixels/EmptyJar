export const mapURLToName = url => {
  for (const name in nameMap) {
    const match = nameMap[name].find(nameUrl => {
      const regex = new RegExp(`(\/|\.|^)${nameUrl}(\/|$)`);
      return regex.test(url);
    });
    if (match) {
      return name;
    }
  }
  return url;
};

